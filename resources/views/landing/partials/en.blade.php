<h4 class="mt-5">Альтернативная модель работы, без доступа исходным кодам</h4>
Если вы уже используете инструменты статического анализа и самостоятельно настроили их запуск на Continuous Integration сервере, вы можете отправлять нам только отчёт о результатах запуска, при этом {{ config('app.name') }} не будет иметь доступа к вашим исходным кодам, не будет иметь доступа к вашему репозиторию.
Мы даём подробные инструкции по настройке отправке отчётов из популярных статически анализаторов, работает на любом CI: GitLab, GitHub Actions, Travis, Bitbucket Pipelines…

<h4 class="mt-5">Вопросы</h4>
<p><strong>Можно ли запускать автоматизированные тесты на {{ config('app.name') }}?</strong></p>
<p>Мы не запускаем автоматизированные тесты. Этим занимается ваш CI. {{ config('app.name') }} проводит только статически анализ
    исходных кодов без их запуска. Но мы можем собирать и визуализировать метрики о результатах автоматизированного
    тестирования, для этого нужно настроить отправку данных с вашего CI в {{ config('app.name') }}. Мы предоставляем подробные
    инструкции для наиболее популярных систем, таких как PHPUnit и Codeception.</p>

<p><strong>Сервис платный?</strong></p>
